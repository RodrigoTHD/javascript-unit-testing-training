export function sum(numberA: number, numberB: number) {
  return numberA + numberB;
}

export function subtract(numberA: number, numberB: number) {
  return numberA - numberB;
}

export function divide(numberA: number, numberB: number) {
  return numberA / numberB;
}

export function multiply(numberA: number, numberB: number) {
  return numberA * numberB;
}
